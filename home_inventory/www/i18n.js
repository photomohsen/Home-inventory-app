let LANG = localStorage.getItem('lang') || 'en';
if (!LANGS.includes(LANG)) LANG = 'en';

export function getLang() { return LANG; }

// t(key, params?) — interpolates {name}, {n}, {who}, etc.
export function t(key, params) {
  const table = DICT[LANG] || DICT.en;
  let str = table[key];
  if (str == null) str = DICT.en[key];
  if (str == null) return key;
  if (params) {
    str = str.replace(/\{(\w+)\}/g, (m, p) => (params[p] != null ? params[p] : m));
  }
  return str;
}

// Pluralisation helper for the count strings (en/da use the same rule; fa too here).
export function tCount(baseKey, n) {
  const key = n === 1 ? baseKey + '_one' : baseKey + '_many';
  return t(key, { n });
}

